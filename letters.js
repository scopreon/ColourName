function drawLetter(letterToUse){
  var c = document.createElement("canvas");
  var div = document.getElementById("word");
  c.width="75";
  c.height="70";
  div.appendChild(c);
  //var c = document.getElementById("myCanvas");
  var letter = letterToUse;
  var ctx = c.getContext("2d");
  var c1 = ctx.fillStyle = '#' + Math.floor(Math.random()*0xffffff).toString(16);
  var c2 = ctx.fillStyle = '#' + Math.floor(Math.random()*0xffffff).toString(16);
  for (let i = 0; i < 14; i++) {
    for (let j = 0; j < 15; j++) {
      if(dict[letter][i][j] == 0){
        ctx.fillStyle = "black";
      }
      else if(dict[letter][i][j] == 1){
        ctx.fillStyle = "blue";
        ctx.fillStyle = c1;
      }
      else if(dict[letter][i][j] == 2){
        ctx.fillStyle = "green";
        ctx.fillStyle = c2;
      }
      else if(dict[letter][i][j] == 3){
        ctx.fillStyle = "gray";
      }
      else{
        ;
      }

      ctx.fillRect(j * 5, i * 5, 5, 5);
    }
  }
}
